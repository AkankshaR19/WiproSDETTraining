package day2;

public class IFelse {
	
	static int marks=75;
	
	public static void main(String[] args) {
		
		if(marks>=90)
		{
			System.out.println("Grade A+");
		}
		else if(marks>=70)
		{
			System.out.println("Grade A");
		}
		else if(marks>=60)
		{
			System.out.println("Grade B");
		}
		else
		{
			System.out.println("Grade C");
		}
		
	}

}
