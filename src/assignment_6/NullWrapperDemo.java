package assignment_6;

public class NullWrapperDemo {
	public static void main(String[] args) {
        Integer num = null;
        Double decimal = null;
        Boolean flag = null;

        System.out.println("Integer value: " + num);
        System.out.println("Double value: " + decimal);
        System.out.println("Boolean value: " + flag);

    }

}
